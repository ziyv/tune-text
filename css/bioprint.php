Agarwal holds a Ph.D. (1987) and an MS in Electrical Engineering from
Stanford University. He got his bachelor's degree in Electrical
Engineering from IIT Madras (1982). He is currently a professor of
Electrical Engineering and Computer Science at MIT, where his teaching
and research interests include VLSI, computer architecture,
compilation, and software systems. He led a group that developed the
MIT Alewife, a scalable shared-memory multiprocessor. He also led the
VirtualWires project at MIT and was founder of Virtual Machine Works,
Inc., which took the VirtualWires logic emulation technology to market.
He currently leads the Raw project in the Computer Science and
Artificial Intelligence Laboratory. He is also founder of InCert
Software Corporation, which is applying a software agent technology to
enterprise applications management.<br><p class="textsectionheader3">Recent and/or Significant Publications:<br><img src="bioprint_files/dotorange.gif" height="1" width="100%"></p>A.
